#include "../pushswap.h"

void	ft_check_limit(long int *arr, int size)
{
	int	i;

	i = 0;
	while (i < size)
	{
		if (arr[i] < -2147483648 || arr[i] > 2147483647)
		{
			write (1, "Error\n", 6);
			free (arr);
			exit (1);
		}
		i++;
	}
}
